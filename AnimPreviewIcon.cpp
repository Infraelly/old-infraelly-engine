#include "AnimPreviewIcon.hpp"
#include "globalFunc.hpp"
#include "Animation.hpp"


AnimPreviewIcon::AnimPreviewIcon():
    anim_(NULL),
    image_(createSurface(235, 150, 32, SDL_SWSURFACE))
{ }

AnimPreviewIcon::~AnimPreviewIcon(){
    if( image_ != NULL ){
        SDL_FreeSurface(image_);
        image_ = NULL;
    }
    anim_ = NULL;
}


void AnimPreviewIcon::setAnim( Animation *anim ){
    anim_ = anim;
}


void AnimPreviewIcon::draw(gcn::Graphics* graphics){
    if( anim_ != NULL ){
        SDL_FillRect(image_, NULL, SDL_MapRGB(image_->format, 255,255,255));
        anim_->draw(image_, 0, 0);
        setImage( loader_.load(image_) );
    }

    gcn::Icon::draw(graphics);
}
