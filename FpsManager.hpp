#ifndef FPSMANAGER_HPP
#define FPSMANAGER_HPP

//  FpsManager.hpp

#include <SDL/SDL.h>


class FpsManager{
    public:
        FpsManager();
        FpsManager(int Fps);
        ~FpsManager();


        //returns the average fps
        int getFps();

        //returns the max FPS allowed
        int getMaxFps();


        // this is the frame capper
        // -1 for unlimited
        void setMaxFps(int newMaxFps);


        //put before u satrt drawing
        void startFrame();

        //put after u finish drawing
        void endFrame();


        // calls endFrame() then startFrame()
        //put at begining of a game loop
        void newFrame();


        //draws Fps to "screen" at "xy"
        void draw(SDL_Surface *dest, int x, int y);



    protected:
        long frameStartTime;
        long capStartTime;
        long nowTime;
        int fps;
        int maxFps;
        int framesDrawn;
};




#endif
